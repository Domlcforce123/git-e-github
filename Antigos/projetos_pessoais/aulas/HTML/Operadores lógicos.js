// operadores logicos (logical operators)

//  -2 valores booleanos, quando verificados,
// resultara em verdadeiro ou falso


let pao = false
let queijo = true

//  AND &&      

console.log(pao && queijo)     // só vira true se tiver os dois

// OR ||

console.log(pao || queijo)  // só vira false se não tiver os dois

// NOT  !   esse é para negar o valor/ trocar o valor

console.log(!pao)